function newProject() {
	var div = document.getElementById('new_project');

	if (div.style.display==='block') {
		div.style.display = 'none';
	} else {
		div.style.display = 'block';
	}
}

function newProjectGroup() {
	var div = document.getElementById('new_path');

	if (div.style.display==='block') {
		div.style.display = 'none';
	} else {
		div.style.display = 'block';
	}
}